    <p class="content_title"><a style="text-decoration:none;" href="http://www.balkan-athletics.eu/2016/istanbul2016.php">Balkan Indoors celebrate participation record</a></p>
    <div class="newsMain">
        
        <p>Istanbul hosted fifth time Balkan Indoors Athletics Championships with record participation from 16 ABAF members plus two guest teams. Romania topped the medal table ahead of Turkey, which achieved their best results in history of the competition.</p>  
        <img src="2016/images/balkanindoors1.jpg">
        <div class="mainHidden" name="sevket">
            <p>Atak&#246;y Athletics Arena, previously hosted to World Indoors 2012, welcomed Balkan nations for the 21st edition of Balkan Indoor Athletics Championships. After ABAF&#39;s (Association of Balkan Athletic Federations) expansion to 16 countries with Israel&#39;s official inclusion, Balkan Indoors have reached record participation figures by terms of countries (16) and athletes (278). The host nation Turkey was leading team in this competition with 47-strong squad meanwhile traditional powers Romania (26 athletes), Greece (25) and Bulgaria (20) also well represented on the field. </p>
            <p>Biggest star of the competition was Serbian world bronze medalist Ivana Spanovic easily clinched her fourth Balkan Indoor gold medal - three of them in Istanbul - after she jumped 6.78 metres, well beyond from second-placed Greek Haido Alexouli. </p>
            <p>In the track, Albanian middle-distance dinamo Luiza Gega also crowned fourth Balkan Indoor title after superb solo run. Gega clocked 4:06.89 on her way of victory, second best time in this distance during European winter season behind Netherland&#39;s Siffan Hassan. </p>
            <img src="2016/images/balkanindoors2.jpg">
            <p style="font-weight:bold;">Double joy for Ivancevic</p>
            <p>Another eye-catching performance drawing by Andrea Ivancevic, who duplicated last year&#39;s feat again. She won 60 metres title in 7.32 despite running low-profile C final, then shines again in hurdles end of the show. The 30-year old Croat repeated her own 2015 feat, when claiming gold in both 60 metres final, hurdles and flat. </p>
            <p>Romania, most successful country of the Balkan Athletics history, topped again the medal table by 16 medals (seven golds, seven silvers, two bronze) ahead of Turkish team. 400 metres runner Adelina Pastor (53.23), shot putter Andrei Gag (20.40m) and Mihai Donisan (2.23m) in high jump was amongst the winners circle in Istanbul. </p>
            <img src="2016/images/balkanindoors3.jpg">
            <p style="font-weight:bold;">Most successful for the hosts</p>
            <p>Turkey also enjoyed very successful event after they celebrated best results ever in Balkan Indoors, obtained 15 medals in total, including six golds. Prominent 19-year old vaulter Demet Parlak missed chance to broke her third national record in one week but possessed Turkey&#39;s first ever Balkan Indoor champion in women&#39;s pole vault. </p>  
            <p>ABAF President Dobromir Karamaninov has delighted by great athmosphere in Istanbul, thanks to Fatih &#199;intimar (TAF President) and his crew to their excellent contribution of Balkan Athletics culture. </p>
            <p>After it&#39;s five-year stop in Istanbul, the championships will move to Belgrade in 2017 as a lead-up event for European Indoor Championships. </p>
