</a></p>
<h6><i>25 February 2017, Belgrade</i></h6>

    <div class="newsMain">

<p>Historical 22nd edition ofthe event took place in Belgrade, in the recently renovated Kombank Arena. It is the first time for Serbia to host a Balkan Indoor Championships, and for the first time a Balkan event serves as a test event for the European Indoor Championships to be held on 3-5 March 2017 in the same venue. More than 300 athletes from 16 Balkan countries tested the new track of the arena.</p>

<img src="/2017/images/Ivana Spanovic winner.JPG">
<h6><i>Photo by Marko Todorovic, Athletich Federation of Serbia</i></h6>

<p>No doubt the biggest star of the competition was Ivana Spanovic (Balkan Athlete of the Year 2016). The gorgeous Serb improved the world leading result for the season before she was stopped at 6.96, which was a strong request for the European title in a few days. </p>

<div class="mainHidden" name="BICH_Belgrde2017">



<img src="/2017/images/Podium PV men.JPG">

<p>The men had a quality competition in Pole Vault won by Ivan Horvat CRO and a national record of 5.76m. Horvat, who was World U20 Vice champion in 2012 defeated the reigning world indoor champion Konstantinos Filippidis GRE (5.71).</p>

<p>Long Jump was another strong event with the men. Izmir Smajlaj of Albania made three corrections of the national record 7.77, 7.80, 7.98, to win the Balkan title.</p>


<img src="/2017/images/SMAJLAJ Izmir LJ.JPG">

<p>Romania got the most medals in Belgrade – 15 and is on the top of the medal table. They had only one title though and are 8th in the team ranking statistics. The most successful team was Greece with 4 gold, 4 silver, 4 bronze medals, followed by Turkey (4-2-2).</p>

