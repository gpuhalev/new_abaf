 <h6><i>5 November 2016 Cuprija, Serbia</i></h6>


    <div class="newsMain">

<p>The 60th edition of the Balkan Cross Country Championships was held on November 5 in Cuprija, Serbia. Turkish athletes dominated in all categories and took all the Team gold medals. Athletes from 13 Balkan countries took part in the championships</p>

<img src="/2016/images/BXCH Cuprija 2016 logo.jpg">

<div class="mainHidden" name="BalkanCross5">

