    <p class="content_title"><a style="text-decoration:none;"
href="http://www.balkan-athletics.eu/2016/dubai2016.php">Balkan Athletes of the Year honoured by royalties</a></p>
 <h6><i>19 November 2016 Dubai, UAE</i></h6>

 <h5><i>Pictures by <a href="https://www.facebook.com/%D0%A1%D0%BF%D0%B8%D1%81%D0%B0%D0%BD%D0%B8%D0%B5-%D0%90%D0%A2%D0%9B%D0%95%D0%A2%D0%98%D0%9A%D0%90-167874983296197/?fref=ts/" target="_blank">Atletika Magazine</a> and <a href="https://www.facebook.com/mohamed.elgarhy.9480?fref=ts" target="_blank">Mohamed Elgarhy</a></i></h5>


    <div class="newsMain">

<p>Balkan Athletics gave its 6th Awards in partnership with the GCC* at a glamorous Gala Ceremony in Dubai. It was a great occasion to promote athletics and celebrate a very successful year where Balkan athletes had won 7 Olympic medals from Rio, 6 medals from the World Indoor Championships in Portland and 23 medals from the European Championships in Amsterdam.</p>

<img src="/2016/images/Balkan Awards 2016.jpg">

 <h6><i>Dobromir Karamarinov with the award winners & Balkan Athletics Vice Presidents Slobodan Brankovic (SRB) and Fatih Cintimar (TUR)</i></h6>

<img src="/2016/images/Balkan Athletics Award winners 2016.jpg">

<p>Ivana Spanovic (SRB) and Yasmani Copello Escobar (TUR)are the Balkan Athletes of the Year 2016. Both are European Champions from Amsterdam and bronze medallists from Rio. 
<br>
Sara Kolak (CRO) and Emmanouil Karalis (GRE) were named Balkan Rising Stars of the Year for their remarkable results. </p>




 <h6><i>*GCC – Gulf Countries Council</i></h6>

<div class="mainHidden" name="GalaDubai">

<img src="/2016/images/UAE governance.jpg">

<p>The Ceremony was hosted by UAE Athletics Federation and took place in the magnificent Atlantis the Palm hotel. It was attended by His Highness Sheikh Ahmad Bin Hashir Al Maktoom, Prime Minister and Ruler of Dubai, himself a sportsman – the Sheikh is the Olympic Champion in shooting from Athens 2004. The General Secretary of Dubai Sport Council His Excellency Saeid Harab was among the guests of honour along with board members of GCC and UAE Athletics. </p>

<img src="/2016/images/Ivana Spanovic Balkan Athlete 2016.jpg">

<p>Ivana Spanovic started the season with a silver medal in long jump at the World Indoor Championships in Portland where she set a national record of 7.07. She won the European gold in Amsterdam and completed the series with a bronze in Rio and a new national record of 7.08. At the end of the season she won the Diamond League and improved the national record once more to 7.10 in her home country. This is the third time for Spanovic to receive the award after having been Balkan Athlete of the Year for 2013 and 2015. </p>

<img src="/2016/images/Yasmani Copello Escobar Balkan Athlete 2016 2.jpg">

<p>Balkan Athlete of the Year Yasmani Copello Escobar is the European Champion in 400m hurdles with a national record of 48:42. In Rio he improved it to 47:92 and won the Olympic bronze medal. He was also part of the wining 4x400m relay of Turkey at the Balkan Senior Championships.</p>

<img src="/2016/images/Emmanouil Karalis Rising Star.jpg">

<p>Rising Star Emmanouil Karalis, who was only 16 years old when he did his performances made an amazing season. He set a World Youth Record of 5.53i in February in Pireas, a month later he improved it to 5.54i in Jablonec, then corrected it once more to 5.55 in May in Ostrava. Karalis is the First European U18 Champion from Tbilisi 2016.</p>


<img src="/2016/images/2016-08-16-Athletics-Javelin-Women-inside-02.jpg">
 <h5><i>picture by Getti Images</i></h5>

<p>Young Sara Kolak (21) surprisingly won the Olympic title in Rio, defeating the favorites in javelin throw and setting a national record of 66.18. She had suggested of her huge potential a few months earlier when she won the bronze at the European Championships in Amsterdam. Unfortunately she was not able to attend the ceremony and receive her Balkan Rising Star award because she was ill.</p>

<img src="/2016/images/DK awarding Sheikh Dubai.jpg">


<p>Dobromir Karamarinov gave a plaque of gratitude to HH Sheikh Al Maktoom for his continuous support to athletics. On his turn he received a sign of appreciation from GCC Athletics President Ahmed Al Kamali. 

<br>Next Balkan Athletics Gala will take place on 16 November 2017 in Stara Zagora, Bulgaria and will be a common activity with GCC again.</p>


