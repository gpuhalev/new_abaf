 <h6><i>Ali Aksu, Vice Presdent of Turkish Athletic Federation and Dobromir Karamarinov open the championships </i></h6>


<p>201 athletes from 16 countries took part in the first edition of the Balkan Indoor U20 Championships in Istanbul on 12 February 2017. A number of athletes realised personal best results and two National U20 records were broken. </p>


<p>The men’s 4x400m relay of Turkey set a National Record of 3:16.94. They actually finished second after the team of Croatia (3:16.40). The other National record was done by Bulgarian Lilyana Georgieva in the girls’ 1500m where she raced unopposed in 4:20.01. Georgieva is only 16 years old and less than an hour after her great performance she also ran in the relay to help Bulgarian team win in 3:49.19. </p>

<img src="/2017/images/relay 4x400 BIU20 2017.jpg">

<p>The hosting Turkish Federation provided excellent conditions for the event including maximum security provisions so that the teams felt safe in Istanbul. The competition was held on higher international standard under the guidance of the Technical Delegate Can Korkmazoglu.</p>

