 <h6><i>30 October 2016 Podgorica</i></h6>

    <div class="newsMain">

<p>The 5th edition of the Balkan Half Marathon Championships took place in Montenegrin capital Podgorica. The race was part of the big Podgorica International Marathon which was ranked among the most prestigious races in the world. Dobromir Karamarinov gave the official opening of the Championships in which 10 Balkan countries took part.</a></p> 

<img src="/images/DK opening BHMC 2016.jpg">

<p>Turkey took the first and third place of the men’s race with Bekir Karayel 1:07:46 and Arslan Ercan, 1:09:34. Karayel is only 21 years old, though has a huge potential. Moldovan Maxim Raileanu won the silver medal with 1:09:34. They were awarded by the President of the Athletic Federation of Montenegro Milorad Vuletic.</p>

<img src="/images/BHMC 2016 Winners Men.JPG">

<p>Never aging Olivera Jevtic (SRB) won the women’s race in 1:12:25. It looks like the race in Podgorica fits her very well as she won the Balkan Marathon last year on the same course. The representative of the hosting country Sladjana Perunovic took the silver medal in Podgorica with a result of 1:16:49. Less than a month ago Sladjana Perunovic won also the Balkan Marathon in Sofia. Turkish Busra Nur Koku took the bronze with 1:18:05.</p>

