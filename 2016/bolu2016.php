<p class="content_title"><a href="#" style="text-decoration:none;">Official Statement of ABAF President regarding participation in Balkan Junior Championships Bolu 2016</a><h6><i>29 June 2016, Sofia</i></h6></p>

 <img src="images/DK Fatih Cintimar.jpg">

           <p>Dear Friends in Balkan Athletics, </p>
<p>We were all shocked and frustrated by the terrorist attack on Istanbul Ataturk airport last night. We know some federations have started to reconsider their participation in the Balkan Junior Championships in Bolu, Turkey on 2-3 July 2016. We understand the worries and fear all participants and their families are experiencing in view of this threat and cannot blame parents for trying to keep their children safe. </p>

<p>On the other hand we cannot let terrorism rule our world and command our activities. Balkans have always been a good example of unity and integrity. Balkan Athletics must remain strong and reliable. We cannot bend under the antihuman challenge of a bunch of confused people. We should stay together and carry on with our scheduled events.</p>


<div class="mainHidden" name="terrorism">

            <p>I hereby call upon all Member Federations to keep on with their activities and send their teams to Bolu, as previously planned. Balkan Junior Championships shall be carried on with reinforced security measures, as announced by the hosting federation. Turkish Federation, Bolu Governorship and Bolu Municipality have invested great efforts and care in the preparation of the event, therefore it is our obligation not to let them down. I am sure you all understand the necessity of a common resistance to terrorism in the world.</p>

<p>I trust in your positive response and remain confident in the success and safeness of the Balkan Junior Championships 2016.</p>

<p>Respectfully, <br>
Dobromir Karamarinov<br>
Balkan Athletics President
</p>
